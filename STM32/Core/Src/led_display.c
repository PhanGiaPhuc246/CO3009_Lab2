void displayLEDMatrix(char c, int index) {
	if (c >= '0' && c <= '9') {
		GPIOB->ODR = (GPIOB->ODR | (setMatrixRow[c - '0'][index] << 8)) & ~(resetMatrixRow[c - '0'][index] << 8);
	}
	else if (c >= 'A' && c <= 'Z') {
		GPIOB->ODR = (GPIOB->ODR | (setMatrixRow[c - 'A' + 10][index] << 8)) & ~(resetMatrixRow[c - 'A' + 10][index] << 8);
	}
	else {
		GPIOB->ODR = (GPIOB->ODR | (setMatrixRow[36][index] << 8)) & ~(resetMatrixRow[36][index] << 8);
	}
}

uint16_t setMatrixEnable[9] = {0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x00};
uint16_t resetMatrixEnable[9] = {0xFE, 0xFD, 0xFB, 0xF7, 0xEF, 0xDF, 0xBF, 0x7F, 0xFF};

void updateLEDMatrix(int index) {
	if (index >= 0 && index <= 7) {
		GPIOB->ODR = (GPIOB->ODR | setMatrixEnable[index]) & ~resetMatrixEnable[index];
	}
	else {
		GPIOB->ODR = (GPIOB->ODR | setMatrixEnable[8]) & ~resetMatrixEnable[8];
	}
}
